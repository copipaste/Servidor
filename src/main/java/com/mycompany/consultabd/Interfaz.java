    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        // nos conectamos al servidor mail por POP3
        pop.conectar("mail.tecnoweb.org.bo");
        // iniciamos sesion mandando las credenciales
        pop.IniciarSesion("grupo20sc", "grup020grup020*");
        // obtenemos la cantidad de mensajes es decir si son 3 mensajes entonces el ultimo seria el de id 3 asi conseguiremos el ultimo mail
        int cantidadMensajes = Integer.parseInt(pop.cantMensajes());
        // una ves que tengamos el id del ultimo mensaje obtenemos el subject de ese mail (getAsunto asi se llama el metodo)
        String subject = pop.getAsunto(cantidadMensajes);
        
        //nos desconectamos del servidor mail ya que obtuvimos lo que necesitabamos
        pop.Desconectar();
        
        // obtenemos el emisor y receptor al cual se le enviara el correo con el resultado de nuestra busqueda de nuestra base de datos
        String em = jTextField13.getText();
        String re = jTextField12.getText();
        
        // consultamos a la base de datos mandando el subject(el asunto) el emisor y el receptor el sibject se utilizara para realizar la busqueda con ese parametro en la base
        // de datos seguido de eso el resultado se enviara al correo del receptor con la informacion del emisor
        postgre.Consultar(subject,em, re);
        
    }//GEN-LAST:event_jButton13ActionPerformed



