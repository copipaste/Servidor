    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
          // Muestra un cuadro de diálogo para pedir al usuario que ingrese el ID
        String input = JOptionPane.showInputDialog(null, "Digite el ID:", "Solicitar ID", JOptionPane.QUESTION_MESSAGE);

        // Verificamos si el usuario ingresó algo y si no canceló el diálogo
        if (input != null && !input.isEmpty()) {
            try {
                // Convertimos el input a un entero si es necesario
                int id = Integer.parseInt(input);
                
                String respuesta = pop.getMensaje(id);
                jTextArea2.setText(respuesta);
                
            } catch (NumberFormatException e) {
                // Si no se puede convertir a entero, mostramos un mensaje de error
                JOptionPane.showMessageDialog(null, "Por favor, ingrese un número válido.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Si el usuario cancela o no ingresa nada
            JOptionPane.showMessageDialog(null, "No se ha ingresado ningún ID.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed

